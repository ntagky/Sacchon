package gr.codehub.sacchon.app.model;

/** The BloodType class is an enum class that represents the  types of blood a patient
    can have. It contains 8 values, 1 for each blood type.

   @author Christos Tzoulias
   @version 1.0
   @since 2023-02-28
 */
public enum BloodType {
    A_POSITIVE,
    A_NEGATIVE,
    B_POSITIVE,
    B_NEGATIVE,
    AB_POSITIVE,
    AB_NEGATIVE,
    O_POSITIVE,
    O_NEGATIVE
}
