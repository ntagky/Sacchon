package gr.codehub.sacchon.app.model;

/** The ConsultationStatus class is an enum class that represents the 4 states of a consultation:

   NONE: No status
   ACTIVE: The consultation is currently active
   EXPIRED: The consultation has expired
   AWAITING: The consultation is pending

   @author Christos Tzoulias
   @version 1.0
   @since 2023-02-28
 */
public enum ConsultationStatus {

    NONE,
    ACTIVE,
    EXPIRED,
    AWAITING

}
